


 import 'package:flutter/material.dart';

 double getHeightByMediaQuery(MediaQueryData mediaQueryData)
{
  return mediaQueryData.size.height;
}

 double getWidthByMediaQuery(MediaQueryData mediaQueryData)
 {
   return mediaQueryData.size.width;
 }

 double getTextSizeByMediaQuery (MediaQueryData mediaQueryData)
 {
   return mediaQueryData.size.width/10;

 }
 double getIconSizeByMediaQuery (MediaQueryData mediaQueryData)
 {
   return mediaQueryData.size.width/7;

 }

